export function interpretBazi(baziChart: BaziChart) {
  const dayElement = baziChart.day.element;
  const yearAnimal = baziChart.year.animal;
  const monthElement = baziChart.month.element;
  const dayMaster = baziChart.dayMaster;

  const elementMeaning = elementMeanings[dayElement];
  const animalMeaning = animalMeanings[yearAnimal];
  const relationInfo = elementRelations[dayMaster];

  const personality = analyzePersonality(dayElement, yearAnimal, monthElement);

  return {
    // ข้อมูลพื้นฐาน
    pillars: {
      year: {
        ...baziChart.year,
        meaning: `ปี${baziChart.year.animal} ธาตุ${baziChart.year.element} ${animalMeaning?.traits || ''}`
      },
      month: {
        ...baziChart.month,
        meaning: `เดือนแห่ง${baziChart.month.animal} ธาตุ${baziChart.month.element} ส่งผลต่อบุคลิกภาพและการแสดงออก`
      },
      day: {
        ...baziChart.day,
        meaning: `วันเกิดของคุณคือวัน${baziChart.day.element} ${elementMeaning?.traits || ''}`
      },
      hour: {
        ...baziChart.hour,
        meaning: `เวลาเกิดของคุณเสริมด้วยธาตุ${baziChart.hour.element} ส่งผลต่อชีวิตภายในและความคิด`
      }
    },
    
    // Day Master
    dayMaster: {
      element: dayMaster,
      name: elementMeaning?.name || dayElement,
      meaning: elementMeaning?.meaning || '',
      traits: elementMeaning?.traits || '',
      career: elementMeaning?.career || '',
      relationships: elementMeaning?.relationships || '',
      description: `คุณเกิดวันธาตุ${dayElement} (${dayMaster}) ${elementMeaning?.traits || ''}`
    },

    // ปีนักษัตร
    yearAnimal: {
      name: animalMeaning?.name || yearAnimal,
      traits: animalMeaning?.traits || '',
      lucky: animalMeaning?.lucky || '',
      career: animalMeaning?.career || '',
      description: `เกิดปี${yearAnimal} ${animalMeaning?.traits || ''}`
    },

    // บุคลิกภาพ
    personality: {
      summary: personality,
      strengths: getStrengths(dayElement),
      weaknesses: getWeaknesses(dayElement),
      lifestyle: getLifestyle(dayElement)
    },

    // ธาตุสนับสนุน
    elements: {
      supportive: relationInfo?.supportive || [],
      avoid: relationInfo?.avoid || [],
      description: relationInfo?.description || '',
      luckyColors: getLuckyColors(dayElement),
      luckyNumbers: getLuckyNumbers(dayElement)
    },

    // บทสรุป
    summary: generateSummary(dayElement, yearAnimal, dayMaster),
    
    // การงานและการเงิน
    career: {
      suitable: elementMeaning?.career || '',
      finance: getFinanceAdvice(dayElement)
    },

    // ความรัก
    love: {
      style: elementMeaning?.relationships || '',
      compatible: getCompatibleAnimals(yearAnimal)
    },

    // สุขภาพ
    health: getHealthAdvice(dayElement)
  };
}

